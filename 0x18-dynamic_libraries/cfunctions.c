#include "main.h"

/**
 * add - adds two numbers
 *
 * @a: first number
 * @b: second number
 *
 * Return: sum of a and b
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - finds the difference between two numbers
 *
 * @a: first number
 * @b: second number
 *
 * Return: the difference between a and b
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - obtains the product of two numbers
 *
 * @a: first nmber
 * @b: second number
 *
 * Return: product of a and b
 */

int mul(int a, int b)
{
	return (a * b);
}


/**
 * mod - find the remainder of the division of two numbers
 *
 * @a: first number
 * @b: second number
 *
 * Return: reminder of division of two numbers
 */

int mod(int a, int b)
{
	return (a % b);
}
